		/// <returns></returns>
		public static byte GenFuncCRC8Table(USE_CRC8_Type crc8Type,byte[] cmd, int length)
		{
			byte nTemp;
			byte nReg = 0;
			UInt32 index = 0;
			if (cmd == null)
			{
				return 0;
			}
			for (index = 0; index < length; index++)
			{
				nTemp = (byte)(nReg ^ cmd[index]);
				switch (crc8Type)
				{
					case USE_CRC8_Type.USE_CRC8_07H:
						nReg = CGenFuncCRC.defaultCRC8Table07H[nTemp];
						break;
					case USE_CRC8_Type.USE_CRC8_31H:
						nReg = CGenFuncCRC.defaultCRC8Table31H[nTemp];
						break;
					default:
						break;
				}
				
			}
			return (byte)nReg;
		}

