		/// </summary>
		/// <param name="cmd"></param>
		/// <param name="length"></param>
		/// <returns></returns>
		public static byte GenFuncCheckSum(byte[] cmd, int length)
		{
			UInt32 i = 0;
			byte _return = 0;
			if (cmd == null)
			{
				return 0;
			}
			for (i = 0; i < length; i++)
			{
				_return += cmd[i];
			}

			//---计算的校验和
			_return = (byte)(0x100 - _return);
			return _return;
		}

