		/// <summary>
		///
		/// </summary>
		/// <param name="cmd"></param>
		/// <param name="length"></param>
		/// <returns></returns>
		public static UInt32 GenFuncCRC32Table(byte[] cmd, int length)
		{
			UInt32 nReg = 0xFFFFFFFF;//CRC寄存器
			UInt32 nTemp = 0;
			UInt32 byteIndex = 0, uIndex = 0;
			if (cmd==null)
			{
				return 0;
			}
			//---开始计算CRC32的值
			for (uIndex = 0; uIndex < length; uIndex++)
			{
				//---强制转换为4字节数据
				nReg ^= (UInt32)cmd[uIndex];

				//---4字节数据计算
				for (byteIndex = 0; byteIndex < 4; byteIndex++)
				{
					//---取一个字节，查表
					nTemp = CGenFuncCRC.defaultCRC32Tab[((nReg >> 24) & 0xFF)];

					//---丢掉计算过的头一个BYTE
					nReg <<= 8;

					//---与前一个BYTE的计算结果异或
					nReg ^= nTemp;
				}
			}
			return nReg;
		}

