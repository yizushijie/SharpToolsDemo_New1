		{
			byte nTemp;
			UInt32 nReg = 0;
			UInt32 index = 0;
			if (cmd==null)
			{
				return 0;
			}
			for (index = 0; index < length; index++)
			{
				nTemp = (byte)((nReg >> 8) ^ cmd[index]);
				nReg = (nReg << 8) ^ CGenFuncCRC.default_crc16_table_1021h[nTemp];
			}
			return (UInt16)nReg;
		}

